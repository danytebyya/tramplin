function toSectionId(title: string, index: number) {
  const slug = title
    .toLowerCase()
    .replace(/[^a-zа-яё0-9]+/gi, "-")
    .replace(/^-+|-+$/g, "");

  return slug || `section-${index + 1}`;
}

function normalizeRawDocument(raw: string) {
  return Object.entries(replacements).reduce((result, [placeholder, value]) => {
    return result.split(placeholder).join(value);
  }, raw);
}

function isTopLevelSection(paragraph: string) {
  return /^(\d+)\.\s+(.+)$/.test(paragraph);
}

function isListCandidate(paragraph: string) {
  return !/^\d+(\.\d+)*\./.test(paragraph);
}

function stripTrailingPunctuation(value: string) {
  return value.replace(/[;.]$/u, "");
}

function parseDocument(raw: string, fallbackTitle: string, description: string): LegalDocument {
  const paragraphs = normalizeRawDocument(raw)
    .split(/\r?\n\r?\n/)
    .map((paragraph) => paragraph.trim())
    .filter(Boolean);
  const topLevelPattern = /^(\d+)\.\s+(.+)$/;
  let title = fallbackTitle;
  const sections: LegalSection[] = [];
  let currentSection: LegalSection | null = null;

  if (paragraphs[0] && !topLevelPattern.test(paragraphs[0])) {
    title = paragraphs.shift() ?? fallbackTitle;
  }

  paragraphs.forEach((paragraph, index) => {
    const topLevelMatch = paragraph.match(topLevelPattern);

    if (topLevelMatch) {
      currentSection = {
        id: toSectionId(topLevelMatch[2], sections.length),
        title: topLevelMatch[2],
        blocks: [],
      };
      sections.push(currentSection);
      return;
    }

    if (!currentSection) {
      currentSection = {
        id: `section-${index + 1}`,
        title: "Общие положения",
        blocks: [],
      };
      sections.push(currentSection);
    }

    const previousBlock = currentSection.blocks[currentSection.blocks.length - 1];
    const previousParagraph =
      previousBlock?.type === "paragraph" ? previousBlock.content : null;

    if (
      previousParagraph &&
      previousParagraph.endsWith(":") &&
      isListCandidate(paragraph)
    ) {
      currentSection.blocks.push({
        type: "list",
        items: [stripTrailingPunctuation(paragraph)],
      });
      return;
    }

    if (
      previousBlock?.type === "list" &&
      isListCandidate(paragraph)
    ) {
      previousBlock.items.push(stripTrailingPunctuation(paragraph));
      return;
    }

    currentSection.blocks.push({
      type: "paragraph",
      content: paragraph,
    });
  });

  return {
    title,
    description,
    sections,
  };
}

